public class Ejercicio_6_1 {
    public static void main(String[] args) {
        double angulo = 45.0;

        System.out.println("Seno del angulo: " + Math.sin(angulo));
        System.out.println("Coseno del angulo: " + Math.cos(angulo));
        System.out.println("Tangente del angulo: " + Math.tan(angulo));
        System.out.println("Arco tangente del angulo: " + Math.atan(angulo));
        System.out.println("Arco tangente 2 del angulo: " + Math.atan2(angulo, angulo));
    }
}
